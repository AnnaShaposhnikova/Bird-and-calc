package org.anna.calk;

public class MyCalk {
    public static int add(int x, int y) {
        int result = x + y;
        return result;
    }

    public static int sub(int x, int y) {
        int result = x - y;
        return result;

    }

    public static int multiplication(int x, int y) {
        int result = x * y;
        return result;

    }

    public static int div(int x, int y) {
        int result = x / y;
        return result;
    }
}

